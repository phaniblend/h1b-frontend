export const searchCities = (query: string): CityState[] => {
  if (!query || query.length < 2) return [];
  
  const searchTerm = query.toLowerCase().trim();
  
  return US_CITIES.filter(city => 
    city.searchKey.includes(searchTerm) ||
    city.city.toLowerCase().includes(searchTerm) ||
    city.state.toLowerCase().includes(searchTerm) ||
    city.stateCode.toLowerCase().includes(searchTerm)
  ).slice(0, 10); // Limit to 10 results for performance
};

export const formatCityState = (city: CityState): string => {
  return `${city.city}, ${city.stateCode}`;
}; 